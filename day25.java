package akram;

public class day25 {
    public static void main(String[] args) {
        //increment  
        for (int i = 1; i <= 9 ; i++) {
            System.out.println("nilai :"+i);
        }
        //decrement
        System.out.println("");
        for (int i = 9; i >= 1; i--) {
            System.out.println("nilai :"+i);

        }
    }
}
